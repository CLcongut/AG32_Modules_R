//TFT GPIO初始化
void TFT_GPIO_Init(void)
{   
    
    //开启时钟
    SYS_EnableAPBClock(APB_MASK_GPIO4);

    //TFT RST GPIO输出配置
    GPIO_SetOutput(TFT_RST_GPIO,TFT_RST_PORT);

    //TFT BLK GPIO输出配置
    GPIO_SetOutput(TFT_BLK_GPIO,TFT_BLK_PORT);

    //TFT DC GPIO输出配置
    GPIO_SetOutput(TFT_DC_GPIO,TFT_DC_PORT);

    //TFT CS GPIO输出配置
    GPIO_SetOutput(TFT_CS_GPIO,TFT_CS_PORT);

    #if USING_SOLFTWARE_SPI
     //TFT SCK GPIO输出配置
    GPIO_SetOutput(TFT_SCK_GPIO,TFT_SCK_PORT);

    //TFT MOSI GPIO输出配置
    GPIO_SetOutput(TFT_MOSI_GPIO,TFT_MOSI_PORT);
    #endif

    //GPIO默认输出高电平
    GPIO_SetHigh(GPIO4,GPIO_BIT3|GPIO_BIT4);
    GPIO_SetHigh(GPIO4,GPIO_BIT5|GPIO_BIT6);

    #if USING_SOLFTWARE_SPI
    GPIO_SetHigh(GPIO4,GPIO_BIT1|GPIO_BIT2);
    #endif

}

/******************************************************************************
      函数说明：LCD串行数据写入函数
      入口数据：dat  要写入的串行数据
      返回值：  无
******************************************************************************/
void LCD_Writ_Bus(uint8_t dat) 
{	
	LCD_CS_Clr();
    SPI0_ReadWriteByte(dat);
	LCD_CS_Set();
}

/******************************************************************************
      函数说明：LCD写入数据
      入口数据：dat 写入的数据
      返回值：  无
******************************************************************************/
void LCD_WR_DATA8(uint8_t dat)
{
	LCD_Writ_Bus(dat);
}


/******************************************************************************
      函数说明：LCD写入数据
      入口数据：dat 写入的数据
      返回值：  无
******************************************************************************/
void LCD_WR_DATA(uint16_t dat)
{
	LCD_Writ_Bus(dat>>8);
	LCD_Writ_Bus(dat);
}


/******************************************************************************
      函数说明：LCD写入命令
      入口数据：dat 写入的命令
      返回值：  无
******************************************************************************/
void LCD_WR_REG(uint8_t dat)
{
	LCD_DC_Clr();//写命令
	LCD_Writ_Bus(dat);
	LCD_DC_Set();//写数据
}


/******************************************************************************
      函数说明：设置起始和结束地址
      入口数据：x1,x2 设置列的起始和结束地址
                y1,y2 设置行的起始和结束地址
      返回值：  无
******************************************************************************/
void LCD_Address_Set(uint16_t x1,uint16_t y1,uint16_t x2,uint16_t y2)
{
		LCD_WR_REG(0x2a);//列地址设置
		LCD_WR_DATA(x1);
		LCD_WR_DATA(x2);
		LCD_WR_REG(0x2b);//行地址设置
		LCD_WR_DATA(y1);
		LCD_WR_DATA(y2);
		LCD_WR_REG(0x2c);//储存器写
}

